let without = function(source, itemsToRemove) {
  let parsedArray = source;
  for (let i = 0; i < source.length; i++) {
    for (let j = 0; j < itemsToRemove.length; j++) {
      if (itemsToRemove[j] === source[j]) {
        parsedArray.splice(j,1);
      }
    }
  }
  return parsedArray;
};

